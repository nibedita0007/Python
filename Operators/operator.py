#Arithmetic operator
a=10
b=5
print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a//b)
print(a**b)

#Relational operator
a=10
b=5
print(a<b)
print(a<=b)
print(a>b)
print(a>=b)
print(a==b)
print(a!=b)

#Logical operator
a=10
b=5
print(a<b and a>b)
print(a<b or a>b)
print(not(a<b and a>b))

#Bitwise operator
a=10
b=5
print(a&b)



#simple assignment operator
a=10
print(a)

#compound assignment operator
a+=10
a-=10
a*=10
print(a)
